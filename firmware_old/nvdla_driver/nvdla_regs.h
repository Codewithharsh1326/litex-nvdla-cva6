/* Group 0 (used for polling — these are the only bits that will ever be
 * non-zero in nv_small since BDMA=0 and RUBIK=0) */
#define NVDLA_GLB_INTR_SDP_DONE (1U << 0)   /* sdp_done_status0  */
#define NVDLA_GLB_INTR_SDP_B_DONE (1U << 1) /* sdp_done_status1  */
#define NVDLA_GLB_INTR_CDP_DONE (1U << 2)   /* cdp_done_status0  */
#define NVDLA_GLB_INTR_CDP_B_DONE (1U << 3) /* cdp_done_status1  */
#define NVDLA_GLB_INTR_PDP_DONE (1U << 4)   /* pdp_done_status0  */
#define NVDLA_GLB_INTR_PDP_B_DONE (1U << 5) /* pdp_done_status1  */
/* bits [6,7] = bdma — always 0 in nv_small */
/* bits [8,9] = rubik — always 0 in nv_small */
/* bits [15:10] = hardwired 0 */
#define NVDLA_GLB_INTR_CDMA_DAT_DONE (1U << 16) /* cdma_dat_done_status0 */
#define NVDLA_GLB_INTR_CDMA_DAT_B_DONE (1U << 17)
#define NVDLA_GLB_INTR_CDMA_WT_DONE (1U << 18) /* cdma_wt_done_status0  */
#define NVDLA_GLB_INTR_CDMA_WT_B_DONE (1U << 19)
#define NVDLA_GLB_INTR_CACC_DONE (1U << 20) /* cacc_done_status0 */
#define NVDLA_GLB_INTR_CACC_B_DONE (1U << 21)
